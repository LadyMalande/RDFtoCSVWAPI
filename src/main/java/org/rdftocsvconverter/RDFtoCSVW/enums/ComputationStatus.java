package org.rdftocsvconverter.RDFtoCSVW.enums;

/**
 * Enum representing the status of a computation task.
 */
public enum ComputationStatus {
    /**
     * The computation is currently in progress.
     */
    COMPUTING,

    /**
     * The computation has completed successfully.
     */
    DONE,

    /**
     * The computation has failed.
     */
    FAILED
}
